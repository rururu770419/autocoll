// 都道府県選択時に市区町村を更新
function initAddressSelect() {
    const prefectureSelect = document.getElementById('prefecture');
    const citySelect = document.getElementById('city');
    
    if (!prefectureSelect || !citySelect) return;
    
    prefectureSelect.addEventListener('change', function() {
        const selectedPrefecture = this.value;
        
        // 市区町村をクリア
        citySelect.innerHTML = '<option value="">市区町村</option>';
        
        if (selectedPrefecture && cityData[selectedPrefecture]) {
            cityData[selectedPrefecture].forEach(city => {
                const option = document.createElement('option');
                option.value = city;
                option.textContent = city;
                citySelect.appendChild(option);
            });
        }
    });
    
    // ページロード時に既存の都道府県に対応する市区町村を設定
    if (prefectureSelect.value && cityData[prefectureSelect.value]) {
        const currentCity = citySelect.dataset.currentCity;
        cityData[prefectureSelect.value].forEach(city => {
            const option = document.createElement('option');
            option.value = city;
            option.textContent = city;
            if (city === currentCity) {
                option.selected = true;
            }
            citySelect.appendChild(option);
        });
    }
}

// DOMContentLoaded時に初期化
document.addEventListener('DOMContentLoaded', initAddressSelect);